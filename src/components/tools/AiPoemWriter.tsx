export default function AiPoemWriter() {
  const [topic, setTopic] = useState("");
  const [style, setStyle] = useState("Free Verse");
  const [mood, setMood] = useState("Inspirational");
  const [poem, setPoem] = useState("");
  const [copied, setCopied] = useState(false);

  function generate() {
    setPoem(generatePoem(topic, style, mood));
  }

  function copy() {
    navigator.clipboard.writeText(poem);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  }

  const labelStyle: React.CSSProperties = { fontFamily: "\"Plus Jakarta Sans\", sans-serif", fontWeight: 700, fontSize: "0.875rem", color: "#1A1A1A", display: "block", marginBottom: "0.4rem" };

  return (
    <div className="tool-widget-content">
      <div className="space-y-4">
        <div>
          <label style={labelStyle}>Topic or subject</label>
          <input type="text" value={topic} onChange={e => setTopic(e.target.value)} style={{ fontFamily: "\"Inter\", sans-serif", fontSize: "0.9rem", color: "#1A1A1A", background: "white", border: "1.5px solid #E8E6DE", borderRadius: "0.5rem", padding: "0.5rem 0.75rem", width: "100%", outline: "none" }} placeholder="e.g. the ocean, autumn leaves, coffee" />
        </div>
        <div>
          <label style={labelStyle}>Style</label>
          <div className="flex flex-wrap gap-2">
            {styles.map(s => (
              <button key={s} onClick={() => setStyle(s)} style={{ fontFamily: "\"Plus Jakarta Sans\", sans-serif", fontWeight: 700, fontSize: "0.8rem", padding: "0.35rem 0.85rem", borderRadius: "9999px", border: "2px solid", borderColor: style === s ? "#1B6B45" : "#E8E6DE", background: style === s ? "#1B6B45" : "white", color: style === s ? "white" : "#4B5563", cursor: "pointer" }}>
                {s}
              </button>
            ))}
          </div>
        </div>
        <div>
          <label style={labelStyle}>Mood</label>
          <div className="flex flex-wrap gap-2">
            {moods.map(m => (
              <button key={m} onClick={() => setMood(m)} style={{ fontFamily: "\"Plus Jakarta Sans\", sans-serif", fontWeight: 700, fontSize: "0.8rem", padding: "0.35rem 0.85rem", borderRadius: "9999px", border: "2px solid", borderColor: mood === m ? "#B8860B" : "#E8E6DE", background: mood === m ? "#FFF9E0" : "white", color: mood === m ? "#B8860B" : "#4B5563", cursor: "pointer" }}>
                {m}
              </button>
            ))}
          </div>
        </div>
        <button onClick={generate} className="btn-primary">Write Poem</button>

        {poem && (
          <div style={{ background: "#F7F6F1", borderRadius: "0.875rem", padding: "1.5rem", border: "1.5px solid #E8E6DE" }}>
            <div className="flex justify-between items-center mb-4">
              <span style={{ fontFamily: "\"Caveat\", cursive", fontWeight: 700, fontSize: "1.1rem", color: "#1B6B45" }}>{style} · {mood}</span>
              <button onClick={copy} style={{ fontFamily: "\"Plus Jakarta Sans\", sans-serif", fontWeight: 700, fontSize: "0.8rem", color: copied ? "#1B6B45" : "#4B5563", background: "white", border: "1.5px solid #E8E6DE", borderRadius: "9999px", padding: "0.25rem 0.75rem", cursor: "pointer" }}>
                {copied ? "✓ Copied!" : "Copy"}
              </button>
            </div>
            <pre style={{ fontFamily: "\"Georgia\", serif", fontSize: "1rem", color: "#1A1A1A", whiteSpace: "pre-wrap", lineHeight: 1.9, margin: 0, fontStyle: "italic" }}>{poem}</pre>
          </div>
        )}
      </div>
    </div>
  );
}