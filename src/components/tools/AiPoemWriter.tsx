import { useState } from "react";

const styles = ["Haiku", "Free Verse", "Limerick", "Rhyming Couplets", "Acrostic"];
const moods = ["Happy", "Sad", "Romantic", "Funny", "Inspirational"];

function generatePoem(topic: string, style: string, mood: string): string {
  const t = topic || "the world";
  const moodAdj: Record<string, string[]> = {
    Happy: ["bright","joyful","warm","golden","cheerful"],
    Sad: ["quiet","grey","fading","hollow","still"],
    Romantic: ["tender","soft","longing","sweet","gentle"],
    Funny: ["wobbly","absurd","silly","odd","goofy"],
    Inspirational: ["bold","rising","fierce","endless","strong"],
  };
  const adj = moodAdj[mood] || moodAdj["Happy"];
  const pick = (arr: string[]) => arr[Math.floor(Math.random() * arr.length)];

  if (style === "Haiku") {
    return `${pick(adj)} ${t} blooms\nsilent moments drift away\npeace in every breath`;
  }
  if (style === "Limerick") {
    return `There once was a thing called ${t},\nso ${pick(adj)} it could make your heart glow,\n  it danced through the air,\n  without any care,\nand left us all putting on a show.`;
  }
  if (style === "Rhyming Couplets") {
    return `The ${pick(adj)} spirit of ${t} fills the air,\na feeling so ${pick(adj)}, beyond compare.\nIt lifts our hearts and clears our mind,\nthe kind of ${pick(adj)} joy that\"s hard to find.\nSo let us pause and take it in,\nand let the wonder now begin.`;
  }
  if (style === "Acrostic") {
    const word = topic.toUpperCase().replace(/[^A-Z]/g, "").slice(0, 8) || "POEM";
    const lines: Record<string, string> = {
      P:"Peaceful moments linger in the air",O:"Only beauty stretches everywhere",E:"Every heartbeat echoes something true",M:"Meaning found in everything we do",
      A:"A gentle light begins to softly glow",B:"Beneath the sky where quiet rivers flow",C:"Carried on the wind, a hopeful song",D:"Drifting through the days where we belong",
      F:"Floating on a wave of something real",G:"Graceful as the things we dare to feel",H:"Held within a moment, still and bright",I:"Into every shadow comes the light",
      J:"Just beyond the horizon, something waits",K:"Kindness opens all the hidden gates",L:"Lightly now the world begins to turn",N:"Nothing lost that we have yet to learn",
      Q:"Quietly the universe expands",R:"Reaching out with open, willing hands",S:"Somewhere in the distance, something calls",T:"Time itself a river as it falls",
      U:"Under every star a story sleeps",V:"Vast the ocean, vast the love it keeps",W:"Wandering through wonder, day by day",X:"Xeric lands bloom green along the way",
      Y:"Yesterday and tomorrow meet right here",Z:"Zeal and grace make everything more clear",
    };
    return word.split("").map(l => lines[l] || `${l} — a letter in the poem of life`).join("\n");
  }
  // Free Verse
  return `${t} —\n${pick(adj)} and unhurried,\nit moves through the spaces\nbetween one breath and the next.\n\nNothing is asked of it.\nNothing is owed.\nOnly this: the ${pick(adj)} fact\nof its being here,\nand the way it changes\neverything it touches.`;
}

